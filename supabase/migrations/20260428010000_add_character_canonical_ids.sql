CREATE OR REPLACE FUNCTION pg_temp.ra_canonical_slug(value TEXT)
RETURNS TEXT
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT regexp_replace(
    regexp_replace(lower(trim(value)), '[^a-z0-9]+', '-', 'g'),
    '(^-|-$)',
    '',
    'g'
  )
$$;

