UPDATE public.character_features cf
SET feat_id = ft.id
FROM canonical_feats ft
WHERE cf.feat_id IS NULL
  AND cf.name IS NOT NULL
  AND lower(trim(cf.name)) = ft.lookup_name
  AND lower(coalesce(cf.source, '')) LIKE '%feat%';

UPDATE public.character_features
SET feat_id = pg_temp.ra_canonical_slug(name)
WHERE feat_id IS NULL
  AND name IS NOT NULL
  AND trim(name) <> ''
  AND lower(coalesce(source, '')) LIKE '%feat%';

