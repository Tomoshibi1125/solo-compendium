export interface SandboxNPC {
	id: string;
	name: string;
	title: string;
	faction:
		| "bureau_sentinels"
		| "vermillion_guild"
		| "hollow_choir"
		| "free_ascendants"
		| "gloamreach_bound";
	level: number;
	job: string;
	hp: number;
	ac: number;
	description: string;
	personality: string;
	motivation: string;
	backstory: string;
	keyAbilities: string[];
	recruitCondition: string;
	isRecruitable: boolean;
	guildAffiliation: string | null;
	location: string;
	questHook: string | null;
	leveling: {
		xp: number;
		xpToNextLevel: number;
		autoLevel: boolean;
		maxLevel: number;
		hpPerLevel: number;
		levelAbilities: Record<number, string>;
	};
}

type NpcInput = Omit<SandboxNPC, "leveling"> & {
	leveling?: Partial<SandboxNPC["leveling"]>;
};

const makeNpc = (npc: NpcInput): SandboxNPC => ({
	...npc,
	leveling: {
		xp: 0,
		xpToNextLevel: 1000,
		autoLevel: true,
		maxLevel: 10,
		hpPerLevel: 6,
		levelAbilities: {
			4: "Field Adaptation",
			7: "Domain-Hardened Resolve",
			10: "Final Assault Bond",
		},
		...npc.leveling,
	},
});

